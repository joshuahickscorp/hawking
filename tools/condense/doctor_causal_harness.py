def selfcheck_fails_when_broken() -> bool:
    """Mutate WHICH component is teacher and confirm an assertion fires."""
    try:
        selfcheck(break_composition=True, verbose=False)
    except AssertionError:
        return True
    return False


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(description=__doc__.split("\n")[0])
    ap.add_argument("--self-check", action="store_true")
    ap.add_argument("--report", type=str, default=None)
    a = ap.parse_args(argv)
    if not a.self_check:
        ap.print_help()
        return 0
    out = selfcheck(verbose=a.report is None)
    out["self_check_fails_when_broken"] = selfcheck_fails_when_broken()
    if a.report:
        os.makedirs(os.path.dirname(a.report), exist_ok=True)
        with open(a.report, "w") as fh:
            json.dump(out, fh, indent=2)
        print(f"wrote {a.report}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
