"""Doctor Prime Part V - reusable CAUSAL INTERVENTION harness for qwen3_moe-shaped parents.

WHY. The Qwen campaign's single most informative measurement was not a weight-error study, it was
the pair of causal controls D1/D2: hold everything at teacher fidelity except ONE named component,
run the real forward, read the loss off the output distribution. Those controls were hand-wired
into the campaign ladder and died with it. This module makes them a parameterised API so the next
parent is diagnosed on day one instead of month three.

WHAT IT COMPOSES. An Intervention names, per block, which components are supplied by the STUDENT
(i.e. LESIONED); everything else is supplied by the teacher:

    attention | router | experts | norms | final_norm | lm_head

plus three surgical extras:

    router_mask            restrict routing to a survivor subset (the D1 lesion)
    teacher_tensors        exact critical-tensor substitution by name/prefix, overriding everything
    reset_to_teacher_after hidden-state reset: after block k, x_student := x_teacher (lockstep)

From those primitives every boundary the campaign needed falls out:

    teacher router + student experts      Intervention(student={"experts"})
    student router + teacher outputs      Intervention(student={"router"})
    teacher attention + student MoE       Intervention(student={"router","experts"})
    student attention + teacher MoE       Intervention(student={"attention"})
    teacher organ injection (per organ)   drop that organ from student_components
    per-block scoping                     student_blocks=(...)
    exact critical-tensor substitution    teacher_tensors=("model.layers.3.mlp.experts.",)
    teacher final norm / unembed          leave "final_norm"/"lm_head" out of student_components
    hidden-state reset after block k      reset_to_teacher_after=(k,)

DEGRADATION PROPAGATION GRAPH (the point of the whole file). Two block-scoped sweeps over the same
driver answer the question "where do I treat?" instead of "which tensor is loudest":

    prefix_teacher[k]   blocks 0..k teacher, k+1.. student   -> damage still generated by the TAIL
    suffix_teacher[k]   blocks 0..k student, k+1.. teacher   -> damage the teacher tail CANNOT wash
                                                                out, i.e. IRREVERSIBLE damage

ONSET is the first block whose marginal contribution to prefix_teacher exceeds a fraction of the
total; IRREVERSIBLE is the first k at which suffix_teacher stops returning to teacher quality.

HONESTY BOUNDARY. This file is validated against a SYNTHETIC TWIN (a 2-layer / 4-expert / hidden-16
qwen3_moe with random bf16 weights on real safetensors shards, the same fixture as
tests/test_qwen_real_forward.py). The twin validates the MECHANISM - that composition, masking,
substitution and hidden-state reset do exactly what they claim - and NOTHING about any parent.
Random weights carry no capability, so every number the self-check prints is a mechanism receipt,
not a science result. symKL / argmax on a calibration batch remain OUTPUT-SPACE PROXIES; they are
losses and are reported as losses.

BIT LAW. This harness is pure diagnosis. It proposes, schedules and promotes no payload, so it
consumes no share of the 1.0 bits/weight ceiling. Any treatment it motivates must still be billed
at INSTALLED bits through tools/foundry/one_bit_ceiling.py.
"""
